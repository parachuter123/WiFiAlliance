function getAreaList(superid) {
    var orderlist = new Array();

    for (var i in AreaList) {
        if (AreaList[i].superid == superid) {
            orderlist.push(AreaList[i]);
        }
    }

    return orderlist;
}


function getAreaSelect() {
    var orderlist = getAreaList(0);

    var htmlstr = "";
    for (var indu in orderlist) {
        htmlstr = htmlstr + "<option value ='" + orderlist[indu].id + "'>" + orderlist[indu].name + "</option>";
    }
    document.write(htmlstr);
}


function showNextAreaSelect(x) {

    var sls = document.getElementById("AreaSelectDIV").getElementsByTagName('select');
    var count = sls.length;
    if (count > 1) {
        for(var i=0;i< count;i++){
            if (sls[i - (count - sls.length)].id != "AreaSelect" && !compare(sls[i - (count - sls.length)].id,x)) {
                document.getElementById("AreaSelect").parentNode.removeChild(sls[i - (count - sls.length)]);
            }
        }
    }


    var orderlist = getAreaList(x);
    if (orderlist.length > 0) {
        var nextselect = document.createElement("select");
        nextselect.id = x;
        nextselect.onchange = function(){
            showNextAreaSelect(this.options[this.selectedIndex].value);
        };
        for (var ind in orderlist) {
            nextselect.options.add(new Option(orderlist[ind].name, orderlist[ind].id));
        }
        document.getElementById("AreaSelect").parentNode.appendChild(nextselect);
        document.getElementById("ProCity").value = nextselect.options[nextselect.selectedIndex].value;
    }else{
        document.getElementById("ProCity").value = x;
    }
}

function compare(a,b){

    if(a==b){
        return false;
    }
    a = a.substring(0,2);
    b = b.substring(0,2);

    if(a == b){
        return true;
    }
    return false;
}

